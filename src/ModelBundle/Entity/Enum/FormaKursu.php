<?php

namespace ModelBundle\Entity\Enum;

use Doctrine\ORM\Mapping as ORM;

/**
 * FormaKursu
 *
 * @ORM\Table(name="enum_forma_kursu")
 * @ORM\Entity(repositoryClass="ModelBundle\Repository\Enum\FormaKursuRepository")
 */
class FormaKursu
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;


    /**
     * Get id.
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name.
     *
     * @param string $name
     *
     * @return FormaKursu
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name.
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    public function __toString()
    {
        return $this->name;
    }


}
